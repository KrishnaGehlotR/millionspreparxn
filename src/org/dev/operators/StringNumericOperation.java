package org.dev.operators;

public class StringNumericOperation {

	public static void main(String[] args) {
		System.out.println("Hi, Welcome all " + "to java class");
		System.out.println("Sum of 4 & 5 is "+ 4+5);
		System.out.println("Sum of 4 & 5 is "+(4+5));
		System.out.println(4+5 +" is the sum of 4 & 5");
		System.out.println();
		System.out.println("Hello");
		System.out.println("Bangalore");
		int i=0;
		System.out.println(i);
		System.out.println(i=10);
	}
}